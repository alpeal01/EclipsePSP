package es.florida.examen;

public class Proteina {
	
	int num;
	String tipo;
	double time;
	
	public Proteina(int num, String tipo, double time) {
		super();
		this.num = num;
		this.tipo = tipo;
		this.time = time;
	}

	public int getNum() {
		return num;
	}

	public String getTipo() {
		return tipo;
	}

	public double getTime() {
		return time;
	}
	
	
	
	
	

}
